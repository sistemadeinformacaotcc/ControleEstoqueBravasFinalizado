    private void jButton1Alterar_FuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1Alterar_FuncActionPerformed
        //Codigo Alterar Usuario
      if(this.jPasswordField1Senha.getText().equals("")){      
        JOptionPane.showMessageDialog(null, "Campo Senha está vazio, coloque uma Senha!");
      }else{  
        try {
            
            login.setUsuario(jTextField1Usuario.getText());
            login.setSenha(jPasswordField1Senha.getText());
            login.setNome(jTextField2nome.getText());
            login.setId_Login(Integer.parseInt(jTextField1Id_Login.getText()));
            daoL.Alterar(login);
        JOptionPane.showMessageDialog(null, "Dados alterado com sucesso");
        
        } catch (DadosInvalidoException ex) {
            JOptionPane.showMessageDialog(null, "Dados Invalidos");
        } catch (ConexaoException ex) {
            Logger.getLogger(CadastroLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DaoException ex) {
            Logger.getLogger(CadastroLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        limpar();
                
        try {
            preecherTabela("select * from login order by id_login");
        } catch (ConexaoException ex) {
            Logger.getLogger(CadastroLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CadastroLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jButton1Alterar_FuncActionPerformed

