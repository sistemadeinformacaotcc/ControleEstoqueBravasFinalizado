public void preecherTabela(String Sql) throws ConexaoException, SQLException{
   // Metodo preencher tabela com dados do banco
    ArrayList dados = new ArrayList();
    String [] colunas = new String[] {"COD","NOME","LOGIN"};
    con.conectar();
    con.executaSql(Sql);
       try{
            con.rs.first();
            do{   
            dados.add(new Object[]{con.rs.getString("id_login"),con.rs.getString("nome"),con.rs.getString("usuario")});
               }while(con.rs.next());
            
       }catch (SQLException ex){
           JOptionPane.showMessageDialog(null, " Erro ao peencher o ArreyList!\nERRO"+ ex);
       } 
       
       DefaultTableCellRenderer rendererCentro = new DefaultTableCellRenderer();
       rendererCentro.setHorizontalAlignment(SwingConstants.CENTER);
       DefaultTableCellRenderer rendererDireita = new DefaultTableCellRenderer();
       rendererCentro.setHorizontalAlignment(SwingConstants.RIGHT);
       DefaultTableCellRenderer rendererEsquerda = new DefaultTableCellRenderer();
       rendererCentro.setHorizontalAlignment(SwingConstants.LEFT);
       ModelTable modelo = new ModelTable(dados, colunas);
       jTable1Pesquisa_usuario.setModel(modelo);
       jTable1Pesquisa_usuario.getColumnModel().getColumn(0).setMaxWidth(80);
       jTable1Pesquisa_usuario.getColumnModel().getColumn(1).setMaxWidth(320);
       jTable1Pesquisa_usuario.getColumnModel().getColumn(2).setMaxWidth(150);
       jTable1Pesquisa_usuario.getTableHeader().setReorderingAllowed(false);
       jTable1Pesquisa_usuario.setAutoResizeMode(jTable1Pesquisa_usuario.AUTO_RESIZE_SUBSEQUENT_COLUMNS);
       jTable1Pesquisa_usuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       con.desconectar();
}

